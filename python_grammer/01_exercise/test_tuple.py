# 元组支持嵌套
t1 = ((1, 2, 3), (1, 2, 3))
print(t1[0][0])

# 定义3个不同数据类型元素的元组
t2 = (1, "hello", True)

# 元组只有一个数据, 这个数据后面要添加逗号
t3 = ("hello",)  # 必须带有逗号, 否则不是元组类型

# 不可以修改元组的内容, 否则直接报错
t4 = (1, 2, 3)
# t4[0] = 5

# 遍历
# 方式一: while
t5 = (1, 2, 3, 4, 5)
index = 0
while index < len(t5):
    # print(t5[index])
    index += 1

# 方式二: for
t6 = (1, 2, 3, 4, 5)
for i in t6:
    print(i)
